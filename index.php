					<h3>Projeto Jovens Urbanos - Cubo Sensorial</h3>

					<p>Com o objetivo de resgatar a tradição do discurso oral e revelar a importância das histórias de vida dos moradores de São Paulo para a construção da identidade cultural da cidade, um dos grupos do programa Jovens Urbanos, do CENPEC, concebeu o projeto Cubo Sensorial.</p>

					<p>Nele, seus idealizadores - adolescentes e jovens associados à ONG Projeto Anchieta - recolheram relatos de moradores de bairros localizados na zona sul de São Paulo, traduzindo suas histórias em exposições itinerantes, apresentadas em um Cubo Sensorial nessas mesmas regiões.</p>

					<p>Paralelamente às exposições, formada por objetos, vídeos e áudios dispostos para ilustrarem as histórias, os relatos coletados foram transcritos em um livro, destinado aos autores e finalizando o ciclo de objetivos do projeto por meio de conteúdos inéditos, apresentados em plataformas diversas.</p>

					
